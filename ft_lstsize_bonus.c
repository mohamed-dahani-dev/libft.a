#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*node;

	if (!lst)
		return ;
	count = 0;
	node = lst;
	while (node != NULL)
	{
		node = node -> next;
		count++;
	}
	return (count);
}
