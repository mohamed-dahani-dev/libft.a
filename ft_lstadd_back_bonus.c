#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!lst)
		return (NULL);
	last = *lst;
	while (last -> next != NULL)
		last = last -> next;
	new = last -> next;
}
